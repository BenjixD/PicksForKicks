public abstract class GoogleSignIn extends AppCompatActivity implements
        GoogleApiClient.OnConnectionFailedListener,
        View.OnClickListener {
    //Static Info
    protected static final String TAG = GoogleSignIn.class.getSimpleName();
    protected static final int RC_SIGN_IN = 9001;

    //Google API clien reference
    protected static GoogleApiClient mGoogleApiClient;
    // [START declare_auth]
    protected static FirebaseAuth mAuth;
    // [END declare_auth]
    // [START declare_auth_listener]
    protected FirebaseAuth.AuthStateListener mAuthListener;
    // [END declare_auth_listener]

    //User Account
    protected static FirebaseUser account;

    @Override
    protected void onCreate(Bundle savedInstanceState) {
        super.onCreate(savedInstanceState);
        setOnAuthStateListener();
    }

    // [START on_start_add_listener]
    @Override
    public void onStart() {
        super.onStart();
        mAuth.addAuthStateListener(mAuthListener);
    }
    // [END on_start_add_listener]

    // [START on_stop_remove_listener]
    @Override
    public void onStop() {
        super.onStop();
        if (mAuthListener != null) {
            mAuth.removeAuthStateListener(mAuthListener);
        }
    }
    // [END on_stop_remove_listener]

    // Failed Connection Google Play Services
    @Override
    public void onConnectionFailed(@NonNull ConnectionResult connectionResult) {
        // An unresolvable error has occurred and Google APIs (including Sign-In) will not
        // be available.
        Log.d(TAG, "onConnectionFailed:" + connectionResult);
        Toast.makeText(this, "Google Play Services error.", Toast.LENGTH_SHORT).show();
    }

    //Declare your own custom onAuthStateListener
    protected abstract void setOnAuthStateListener();
}
